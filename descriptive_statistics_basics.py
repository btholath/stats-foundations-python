import statistics as st

data = [1,2, 3, 3]

# Mean calculation
mean = st.mean(data)
print("Mean =", mean)


# Median
median = st.median(data)
print("Mean =", mean)

# standard deviation
std_dev = st.stdev(data)
print("Standard Deviation =", std_dev)

# variance
variance = st.variance(data)
print("Variance =", variance)

